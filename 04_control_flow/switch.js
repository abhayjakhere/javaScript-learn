// switch statement is used to check various conditions
// it check value for each case and if its true it will execute the given task and break or continue as per instructions


let month = 7
switch (month) {
    case 1:
        console.log("its january");
        break;
    case 2:
        console.log("its February");
        break;
    case 3:
        console.log("its March");
        break;
    case 4:
        console.log("its April");
        break;
    case 5:
        console.log("its May");
        break;
    case 6:
        console.log("its June");
        break;
    case 7:
        console.log("its July");
        break;
    case 8:
        console.log("its August");
        break;
    case 9:
        console.log("its September");
        break;
    case 10:
        console.log("its October");
        break;
    case 11:
        console.log("its November");
        break;
    default:
        console.log("its December");
        break;
}